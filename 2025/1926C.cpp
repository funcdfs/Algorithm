const int N = 2e5 + 10;
int64 f[N];

int dig(int x) {
   int cnt = 0;
   while (x / 10) {
      cnt += x % 10;
      x /= 10;
   }
   if (x) {
      cnt += x;
   }
   return cnt;
}

int main() {
   for (int i = 1; i < N; i++) {
      f[i] = f[i - 1] + dig(i);
   }
   int t;
   cin >> t;
   while (t--) {
      int n;
      cin >> n;
      cout << f[n] << "\n";
   }
   return 0;
}

